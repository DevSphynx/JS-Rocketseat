// Operadores Aritiméticos

// Multiplicação
console.log(2*2);

// Divisão
console.log(2/2);

// Soma
console.log(2+2);

// Subtração
console.log(2-2);

// Resto da divisão / Remainder
console.log(6%5)

// Incremento ++
n = 1
console.log(++n) // ++ antes de n, soma e printa, ++ pós n, printa n e depois soma

// Decremento --
console.log(--n)

// Exponencial
console.log(2**3)
